import {GiSettingsKnobs as icon} from 'react-icons/gi'
import {defineArrayMember, defineField, defineType} from 'sanity'

export const settings = defineType({
	name: 'settings',
	title: 'Settings and Menus',
	type: 'document',
	icon,
	// Uncomment below to have edits publish automatically as you type
	// liveEdit: true,
	fields: [
		defineField({
			name: 'menuItems',
			title: 'Menu Item list',
			description: 'Links displayed on the header of your site.',
			type: 'array',
			of: [
				defineArrayMember({type: 'navCTA'}),
				defineArrayMember({type: 'navDropdownCTA'}),
				defineArrayMember({type: 'blogLandingPage'}),
			],
		}),
		defineField({
			name: 'footerLogos',
			title: 'Footer Logos',
			description: 'These are the accreditations that appear in the footer.',
			type: 'array',
			of: [defineArrayMember({type: 'logo'})],
		}),
		defineField({
			name: 'ogImage',
			title: 'Open Graph Image',
			type: 'image',
			description: 'Displayed on social cards and search engine results.',
			options: {
				hotspot: true,
				aiAssist: {
					imageDescriptionField: 'alt',
				},
			},
			fields: [
				defineField({
					name: 'alt',
					description: 'Important for accessibility and SEO.',
					title: 'Alternative text',
					type: 'string',
					validation: (rule) => {
						return rule.custom((alt, context) => {
							if ((context.document?.ogImage as any)?.asset?._ref && !alt) {
								return 'Required'
							}
							return true
						})
					},
				}),
				defineField({
					name: 'metadataBase',
					type: 'url',
					description: (
						<a
							href="https://nextjs.org/docs/app/api-reference/functions/generate-metadata#metadatabase"
							rel="noreferrer noopener"
						>
							More information
						</a>
					),
				}),
			],
		}),
	],
	preview: {
		prepare() {
			return {
				title: 'Menu Items',
			}
		},
	},
})

